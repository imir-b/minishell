#include "utils.h"

char	*ft_strndup(char *src, size_t n)
{
	char	*dest;
	size_t	len;
	size_t	i;

	len = 0;
	while (src[len] && len < n)
		len++;
	dest = malloc(sizeof(char) * (len + 1));
	if (!dest)
		return (NULL);
	i = 0;
	while (i < len)
	{
		dest[i] = src[i];
		i++;
	}
	dest[len] = '\0';
	return (dest);
}
